// ########## ------ IMPORT ------- ##########
#include <iostream>
#include <bits/stdc++.h>
using namespace std;

// ########## ------ DEFINE ------- ##########

// ########## ------ Code ------- ##########
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    long long int t, i, j, k;
    cin >> t;
    int mod = 1e9+7;

    long long dp[t+1];

    memset(dp, 0, sizeof(dp));
    dp[0] = 1;


    for (i=1;i<=t;i++) {
        for (j=1;j<=6;j++) {
            k = i-j;
            if (k<0) {
                break;
            }
            dp[i] = (dp[i]+dp[k])%mod;
            //cout<<dp[i]<<i<<"\n";
        }
    }
    cout<<dp[t];


}